function toggleGalleryToCarousel() {
    const container = document.getElementById("gallery-container");

    // Contenuto del carosello
    const carouselHTML = `
        <div id="galleryCarousel" class="carousel slide" data-bs-ride="carousel">
            <div class="carousel-inner">
                <div class="carousel-item active">
                    <img src="assets/1.webp" class="d-block w-100" alt="Main Image">
                </div>
                <div class="carousel-item">
                    <img src="assets/2.webp" class="d-block w-100" alt="Gallery Image">
                </div>
                <div class="carousel-item">
                    <img src="assets/3.webp" class="d-block w-100" alt="Gallery Image">
                </div>
                <div class="carousel-item">
                    <img src="assets/4.webp" class="d-block w-100" alt="Gallery Image">
                </div>
                <div class="carousel-item">
                    <img src="assets/5.webp" class="d-block w-100" alt="Gallery Image">
                </div>
            </div>
            <button class="carousel-control-prev" type="button" data-bs-target="#galleryCarousel" data-bs-slide="prev">
                <span class="carousel-control-prev-icon" aria-hidden="true"></span>
                <span class="visually-hidden">Previous</span>
            </button>
            <button class="carousel-control-next" type="button" data-bs-target="#galleryCarousel" data-bs-slide="next">
                <span class="carousel-control-next-icon" aria-hidden="true"></span>
                <span class="visually-hidden">Next</span>
            </button>
        </div>
    `;

    // Verifica larghezza schermo
    if (window.innerWidth < 768) {
        // Se lo schermo è piccolo, sostituisci con il carosello
        if (!document.querySelector(".carousel")) {
            container.innerHTML = carouselHTML;
        }
    } else {
        // Ripristina la galleria originale se lo schermo è grande
        if (!document.querySelector(".gallery")) {
            container.innerHTML = `
                <div class="row g-3 property-gallery mt-2 gallery">
                    <div class="col-md-6 Mainmg">
                        <img src="assets/1.webp" alt="Main Image" class="img-hover-dark">
                    </div>
                    <div class="col-md-6">
                        <div class="row g-3">
                            <div class="col-md-6">
                                <img src="assets/2.webp" alt="Gallery Image" class="img-hover-dark">
                            </div>
                            <div class="col-md-6 rtb">
                                <img src="assets/3.webp" alt="Gallery Image" class="img-hover-dark">
                            </div>
                            <div class="col-md-6">
                                <img src="assets/4.webp" alt="Gallery Image" class="img-hover-dark">
                            </div>
                            <div class="col-md-6 rbb">
                                <img src="assets/5.webp" alt="Gallery Image" class="img-hover-dark">
                            </div>
                        </div>
                    </div>
                </div>
            `;
        }
    }
}

// Assegna l'evento "resize" e verifica inizialmente
window.addEventListener("resize", toggleGalleryToCarousel);
window.addEventListener("load", toggleGalleryToCarousel);
